def clear_terminal():
    os.system('cls')

"""
    This function is dynamic. It can be use to apply animation for strings.

    type_of_animation is either "line_by_line" or "char_by_char"
    Can also use the blinking feature
"""
def animate_string(string, type_of_animation, delay = 0.1, blinking = 0):

    string_as_list = string.strip().split('\n')
    
    if type_of_animation == "line_by_line":
        lines = string.strip().split('\n')
        
        for i in range(len(lines)):
            clear_terminal()
            
            lines_to_print = string_as_list[:i + 1]
            for lines in lines_to_print:
                print(lines)

            time.sleep(delay)
            
    elif type_of_animation == "char_by_char":
        pass

